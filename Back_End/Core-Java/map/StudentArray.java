package com.capgemini.map.hashmap;

public class StudentArray
{
int age;
String name;
public StudentArray(int age, String name) {
	super();
	this.age = age;
	this.name = name;
}
/* (non-Javadoc)
 * @see java.lang.Object#toString()
 */
@Override
public String toString() {
	return "StudentArray [age=" + age + ", name=" + name + "]";
}

}
